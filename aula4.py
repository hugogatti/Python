#Tipos int e float
#int -> inteiros representado por qualquer número positivo ou negativo. int sem sinal já é considerado inteiro
print('int')
print(11) # int
print(-11) # int
print(0)

#Float -> Número com ponto flutuante
#Representa qualquer número
#float sem sinal já é considerado inteiro
print('float')
print(1.1, 10.11)

# A função type mostra o tipo que o Python inferiu
print(type('Gatti'))
print(type(1))
print(type(1.5))
print(type(1.5), type(-1.1), type(0.0))